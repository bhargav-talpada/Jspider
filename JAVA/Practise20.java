public class Practise20 {
    public static void squrecube(int n1){
        int numSqure = n1 * n1;
        int numCube = n1 * n1 * n1;
        System.out.println("Squre of " + n1 + " is : "+ numSqure);
        System.out.println("Cube of " + n1 + " is : "+ numCube);
    }
    public static void main(String[] args) {
        squrecube(5);
    }
}

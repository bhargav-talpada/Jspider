public class Practise22 {
    public static void kmtomiles(int km){
        float miles = km * 0.621371f; 
        System.out.println("Kilo Meter is : " + km);
        System.out.println("Miles is : "+ miles);
    }
    public static void main(String[] args) {
        kmtomiles(55);
    }
}
